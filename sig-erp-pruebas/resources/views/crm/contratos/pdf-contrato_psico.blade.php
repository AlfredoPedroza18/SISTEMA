<p style="text-align: justify;">
d) Si LA PROVEEDORA, no guarda estricta reserva en cuanto a la información que recibiere de EL CLIENTE, con motivo de la prestación de los servicios.</p>
<p style="text-align: justify;">
e) Si alguna de las partes incumple con cualquiera de las obligaciones contraídas en el presente contrato o incurre en cualquier violación a las disposiciones y ordenamientos que lo regulan.</p>
