</p>
<p style="text-align: left;">
<strong>
SEXTA. PROPIEDAD INDUSTRIAL Y DERECHOS DE AUTOR.
</strong>
</p>
<p style="text-align: justify;">
LA PROVEEDORA, conviene que todo producto o bien tangible que emane de los servicios, directa o indirectamente, bajo los términos de este Contrato, será propiedad total y a favor de EL CLIENTE, quien adquirirá sobre ellos todos los derechos, títulos e intereses, incluyendo, sin limitación, derechos de autor y secretos industriales.
</p>
<p style="text-align: left;">
<strong>
SEPTIMA. CONFIDENCIALIDAD.</strong>
</p>
<p style="text-align: justify;">
Las partes reconocen que durante la duración del presente contrato, recibirán, conocerán, observarán y tendrán acceso a información y datos considerados confidenciales de las mismas, incluyendo sin limitación todo tipo de secretos industriales, programas de cómputo, ideas, diseños, procesos productivos e industriales, aplicaciones tecnológicas industriales y comerciales, planes de negocios, estrategias, políticas, procedimientos e información de carácter administrativa, contable, financiera, de ventas, de personal, programas promociónales y de mercadeo, métodos de operación, sistemas de administración, listados de clientes y proveedores, fórmulas, resultados de investigaciones, identidad de personal clave, así como cualquier otra información obtenida ya sea por escrito o que se encuentre grabada o registrada en la memoria de cualquier equipo de cómputo de las partes, o de manera ocular o verbal, directa o indirectamente, con motivo de la producción de bienes o de la prestación de los servicios, o de ambas y que será considerada para todos los efectos como confidencial.
</p>
<p style="text-align: justify;">
En esa virtud, las partes se obligan a guardar y mantener la confidencialidad de toda esa información que conste en cualquier clase de documento, material, medio magnético o electrónico, así como las conversaciones y pláticas que sean grabadas o conservadas en cualquier medio magnético o electrónico, documento o material en donde se haga constar la “Información Confidencial”.</p>
<p style="text-align: justify;">
Por lo que deberá ser guardada como confidencial por la parte receptora y no podrá divulgarse por cualquier medio, sin el previo consentimiento por escrito de la parte que haya proporcionado la Información confidencial, es por lo que la parte que la reciba se compromete a:</p>
<p style="text-align: justify;">
a) Proteger dicha información confidencial utilizando todos los medios profesionales aplicables para alcanzar ese fin.</p>
<p style="text-align: justify;">
b) Utilizar la información confidencial únicamente con el objetivo de ejecutar sus obligaciones en el marco de este contrato.</p>
<p style="text-align: justify;">
c) Reproducir información confidencial únicamente en la medida necesaria para ejecutar sus obligaciones en el marco de este contrato.</p>
<p style="text-align: justify;">
Las partes acuerdan a su vez, que la obligación de mantener confidencial la información, pactada en este Contrato, tendrá una validez indefinida y en el supuesto de infringir esos compromisos, estarán expuestos a las sanciones que para ese efecto se contemplan en la Ley de la Propiedad Industrial, la Ley Federal del Derecho de Autor y el Código Penal.</p>
